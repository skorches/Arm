def get_reading_for_day(day_number, year=None):
    """
    Get the reading assignment for a specific day of the year.
    
    Args:
        day_number: Day of year (1-365)
        year: Year to use (defaults to current year)
    
    Returns:
        String with reading assignment or None if not found
    """
    from datetime import datetime
    
    if year is None:
        year = datetime.now().year
    
    if year not in READING_PLANS:
        # Fallback to most recent available year
        year = max(READING_PLANS.keys())
        logger.warning(f"Year {year} not found in reading plans, using {year}")
    
    plan = READING_PLANS[year]
    
    if day_number < 1 or day_number > 365:
        return None
    
    return plan.get(day_number, None)


