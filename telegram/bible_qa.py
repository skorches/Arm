def find_answer(user_question):
    """
    Find the best matching answer for a user's question
    Returns the best match or None if no good match found
    """
    user_question_lower = user_question.lower()
    
    # Score each Q&A entry based on keyword matches
    best_match = None
    best_score = 0
    
    for qa in BIBLE_QA:
        score = 0
        for keyword in qa["keywords"]:
            if keyword in user_question_lower:
                score += 1
        
        if score > best_score:
            best_score = score
            best_match = qa
    
    # Only return if we have a reasonable match (at least 1 keyword)
    if best_score > 0:
        return best_match
    
    return None

